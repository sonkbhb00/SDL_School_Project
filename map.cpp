map::map() {
    dirt = textureManeger::loadTexture("assets\\dirt.jpg");
    grassBlock = textureManeger::loadTexture("assets\\grassBlock.jpg");
    grass = textureManeger::loadTexture("assets\\grass.png");
    loadMap(lv1); // Load the level data into the grid
    srcRect.x = destRect.x = 0;
    srcRect.y = destRect.y = 0;
    srcRect.w = 32;
    srcRect.h = 32;
    destRect.w = 32;
    destRect.h = 32;
;}

void map::loadMap(int arr[30][50]) {
    for (int row = 0; row < 30; row++) {
        for (int column = 0; column < 50; column++) {
            grid[row][column] = arr[row][column];
        }
    }
}
void map::drawMap() {
    int type = 0;
    for (int row = 0; row < 30; row++) {
        for (int column = 0; column < 50; column++) {
            type = grid[row][column];
            destRect.x = column *32;
            destRect.y = row * 32;
            switch (type) {
                case 0:
                    textureManeger::draw(dirt,srcRect,destRect);
                    break;
                case 1:
                    textureManeger::draw(grassBlock,srcRect,destRect);
                // Add other cases as needed
            }
        }
    }
}
